#!/usr/bin/env python
# -*- coding: utf-8 -*-

import dis

print('---------------------------------------------------------------------------')

def f0(a):
    print(a)
    print(b) # name 'b' is not defined

# f0(3)

print('---------------------------------------------------------------------------')

b = 6

def f1(a):
    print(a)
    print(b)

# f1(3)

print('---------------------------------------------------------------------------')

def f2(a):
    print(a)
    print(b) # local variable 'b' referenced before assignment
    b=9

# f2(3)

print('---------------------------------------------------------------------------')

def f3(a):
    global b
    print(a)
    print(b)
    b = 9

# f3(3)

print('---------------------------------------------------------------------------')
print(dis.dis(f0))
print('---------------------------------------------------------------------------')
print(dis.dis(f1))
print('---------------------------------------------------------------------------')
print(dis.dis(f2))
print('---------------------------------------------------------------------------')
print(dis.dis(f3))
print('---------------------------------------------------------------------------')


